def is_numeric(s: str, n: int) -> bool:
    for i in range(n):
        if s[i] not in '0123456789':
            return False
    return True

def main():
    s = "1234"
    length = len(s)
    if is_numeric(s, length):
        print("String is Numeric")
    else:
        print("String is not Numeric")

if __name__ == "__main__":
    main()





