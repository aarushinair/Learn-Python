def remove(string, i): 

	# Characters before the i-th indexed 
	# is stored in a variable a 
	a = string[ : i] 
	
	# Characters after the nth indexed 
	# is stored in a variable b 
	b = string[i + 1: ] 
	
	# Returning string after removing 
	# nth indexed character. 
	return a + b 
